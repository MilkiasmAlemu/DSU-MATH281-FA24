# ╔═╡ 8d3dd03b-9f82-42d8-adde-93d1e8b1b755
X = DiscreteUniform(1,6)

# ╔═╡ c5ba6d39-5baa-4d8e-ad74-8be9a7c4def1
Y = DiscreteUniform(1,6)

# ╔═╡ fd1dcdc9-57ba-4966-bcb7-f66dc44b89ed
begin
	local x_values = range(max(minimum(X), 0), min(maximum(X),10), step = 1)
	local y_values = range(max(minimum(Y), 0), min(maximum(Y),10), step = 1)
	
	local pmf = [pdf(X, x) * pdf(Y, y) for x in x_values, y in y_values]
	
	local x_values_js = collect(x_values)
	local y_values_js = collect(y_values)
	local pmf_js = [[pmf[i, j] for j in 1:length(y_values)] for i in 1:length(x_values)]
	
	@htl("""
		<canvas id="pmf-plot" width="600" height="500" style="border: 1px solid black;"></canvas>
		
		<script>
			const x_vals = $(x_values_js);
			const y_vals = $(y_values_js);
			const pmf_data = $(pmf_js);
			
			const canvas = document.getElementById('pmf-plot');
			const ctx = canvas.getContext('2d');
			
			const width = canvas.width;
			const height = canvas.height;
			
			let offsetX = 100;
			let offsetY = height / 2;
			const scale = 30;
			
			let isDragging = false;
			let startX, startY;
			
			let angleX = - Math.PI + 0.1;
			let angleY = Math.PI / 4 + 0.1;
			
			function fillBackground() {
				ctx.fillStyle = 'white';
				ctx.fillRect(0, 0, width, height);
			}
			
			function rotate3D(x, y, z) {
				const cosY = Math.cos(angleY);
				const sinY = Math.sin(angleY);
				const cosX = Math.cos(angleX);
				const sinX = Math.sin(angleX);
				
				const x1 = cosY * x + sinY * z;
				const z1 = -sinY * x + cosY * z;
	
				const y1 = cosX * y - sinX * z1;
				const z2 = sinX * y + cosX * z1;
				
				return [x1, y1, z2];
			}
			
			function projectPoint(x, y, z) {
				return [offsetX + x, offsetY - y];
			}
			
			function drawAxes() {
				ctx.strokeStyle = 'black';
				ctx.lineWidth = 3;
				ctx.beginPath();
	
				const [x1, y1] = projectPoint(...rotate3D(scale * x_vals.length, 0, 0));
				ctx.moveTo(offsetX, offsetY);
				ctx.lineTo(x1, y1);
				
				const [x2, y2] = projectPoint(...rotate3D(0, 0, scale * 10));
				ctx.moveTo(offsetX, offsetY);
				ctx.lineTo(x2, y2);
				
				ctx.stroke();
			}
			
			function drawPmf() {
				ctx.strokeStyle = 'rgba(0,0,255,0.75)';
				ctx.lineWidth = 10;
				for (let i = 0; i < x_vals.length; i++) {
					for (let j = 0; j < y_vals.length; j++) {
						const x = i * scale;
						const y = 0;
						const z = j * scale;
						
						const barHeight = pmf_data[i][j] * 2000;
						
						const [x_rot, y_rot, z_rot] = rotate3D(x, y, z);
						const [x_rot_top, y_rot_top, z_rot_top] = rotate3D(x, y - barHeight, z);
						
						ctx.beginPath();
						const [projX1, projY1] = projectPoint(x_rot, y_rot, z_rot);
						const [projX2, projY2] = projectPoint(x_rot_top, y_rot_top, z_rot_top);
						ctx.moveTo(projX1, projY1);
						ctx.lineTo(projX2, projY2);
						ctx.stroke();
					}
				}
			}
			
			function redraw() {
				fillBackground();
				drawAxes();
				drawPmf();
			}
			
			canvas.addEventListener('mousedown', function(event) {
				isDragging = true;
				startX = event.clientX - canvas.offsetLeft;
				startY = event.clientY - canvas.offsetTop;
			});
			
			canvas.addEventListener('mousemove', function(event) {
				if (isDragging) {
					const mouseX = event.clientX - canvas.offsetLeft;
					const mouseY = event.clientY - canvas.offsetTop;
					
					const deltaX = mouseX - startX;
					const deltaY = mouseY - startY;
					
					angleY += deltaX * 0.01;
					angleX += deltaY * 0.01;
					
					startX = mouseX;
					startY = mouseY;
					
					redraw();
				}
			});
			
			canvas.addEventListener('mouseup', function() {
				isDragging = false;
			});
			
			canvas.addEventListener('mouseout', function() {
				isDragging = false;
			});
	
			redraw();
		</script>
	""")
end

